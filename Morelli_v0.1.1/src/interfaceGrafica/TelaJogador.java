    private void processa(Posicao posicao) {
        if (posicao != posicaoOrigem) {
            if (!origemSelecionada) {
                if (posicao.isOcupada()) {
                    posicaoOrigem = posicao;
                    origemSelecionada = true;
                }
            } else {
                posicaoDestino = posicao;
                jogo.movimentarPeca(posicaoOrigem, posicaoDestino);
//            enableEditar(painelBotoes);
                origemSelecionada = false;
            }
        }
    }

